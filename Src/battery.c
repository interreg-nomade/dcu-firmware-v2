void measBatt_Init()
{
	HAL_ADC_MspInit(&hadc3);

	MX_ADC3_Init();
}

void measBatt(float *value)
{
	HAL_ADC_Start(&hadc3);

	float result = 0;

	for(uint8_t i=0; i<10; i++)
	{
		HAL_ADC_PollForConversion(&hadc3, 10);
		uint16_t AD_RES = HAL_ADC_GetValue(&hadc3);
		result += (float) AD_RES;
	}
	result /= 10;

	HAL_ADC_Stop(&hadc3); // stop adc

	// 100k - 10k
	float R1 = 100.0f; // 100k
	float R2 = 10.0f; // 10k
	float VDD = 3.3f; // 3V3
	float r_div = ( R1 + R2 ) / R2;
	float voltage = (float) result / (float) 0xFFFF * VDD * r_div * 0.92;

	*value = voltage;
}


